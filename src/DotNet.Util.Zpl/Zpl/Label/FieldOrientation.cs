namespace DotNet.Util.Zpl.Label
{
    /// <summary>
    /// Field Orientation
    /// </summary>
    public enum FieldOrientation
    {
        /// <summary>
        /// Normal
        /// </summary>
        Normal,
        /// <summary>
        /// Rotated 90 degrees (clockwise)
        /// </summary>
        Rotated90,
        /// <summary>
        /// Inverted 180 degrees
        /// </summary>
        Rotated180,
        /// <summary>
        /// Read from bottom up, 270 degrees
        /// </summary>
        Rotated270
    }
}
